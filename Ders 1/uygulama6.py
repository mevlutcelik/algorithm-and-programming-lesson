# Dandik bir bilgisayarın fiyatı 10400TL'dir. KDV li fiyatını hesaplayınız
# IPhone telefonun fiyatı 57000TL'dir. KDV'li fiyatını hesaplayınız
# KDV'i 0.18 (%18 alınız)
pc = 10400
iphone = 57000

print("Telefonun KDV'li Fiyatı:\t")
print(pc * (pc * 0.18))

print("###############")

print("IPhone'in KDV'li Fiyatı:\t")
print(iphone * (iphone * 0.18))