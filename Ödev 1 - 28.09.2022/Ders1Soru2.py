#FLOAT NEDİR?
# Büyük ve ondalıklı sayılar tanımlanabilen veri türüdür. 
# 32 Bit boyutundadır ve 1.4×10^-45 ile 3.4×10^38 aralığında bir değer tanımlanabilir. Float içerisine tam sayı yazdığımız zamanda bile o sayı 1.0 şeklinde ondalıklı olarak algılar.